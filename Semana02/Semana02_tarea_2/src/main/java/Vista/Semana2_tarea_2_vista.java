    // üëâ M√©todo 1: Ingresar datos
    private boolean ingresarDatos() {
        try {
            horasTrabajadas = Integer.parseInt(txtHoras.getText());
            hijos = Integer.parseInt(txtHijos.getText());
            categoria = cboCategoria.getSelectedItem().toString();

            if (categoria.equals("Seleccione una categor√≠a")) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una categor√≠a v√°lida.");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese valores v√°lidos (n√∫meros).");
            return false;
        }
        return true;
    }

    // üëâ M√©todo 2: Calcular sueldos
    private void calcular() {
        // Sueldo b√°sico
        if (categoria.equals("A")) {
            sueldoBasico = horasTrabajadas * 45.0;
        } else if (categoria.equals("B")) {
            sueldoBasico = horasTrabajadas * 37.5;
        }

        // Bonificaci√≥n por hijos
        if (hijos <= 3) {
            bonificacion = hijos * 40.5;
        } else {
            bonificacion = hijos * 35.0;
        }

        // Sueldo bruto
        sueldoBruto = sueldoBasico + bonificacion;

        // Descuento
        if (sueldoBruto >= 3500) {
            descuento = sueldoBruto * 0.135;
        } else {
            descuento = sueldoBruto * 0.10;
        }

        // Sueldo neto
        sueldoNeto = sueldoBruto - descuento;
    }

    // üëâ M√©todo 3: Mostrar resultados
    private void mostrarResultados() {
        txtSalida.setText("\tResultados");
        txtSalida.append("\n--------------------------------");
        txtSalida.append("\nHoras trabajadas: " + horasTrabajadas);
        txtSalida.append("\nCategor√≠a: " + categoria);
        txtSalida.append("\nN√∫mero de hijos: " + hijos);
        txtSalida.append("\nSueldo B√°sico: " + sueldoBasico);
        txtSalida.append("\nBonificaci√≥n: " + bonificacion);
        txtSalida.append("\nSueldo Bruto: " + sueldoBruto);
        txtSalida.append("\nDescuento: " + descuento);
        txtSalida.append("\nSueldo Neto: " + sueldoNeto);
    }

